    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        if(doctorRb.isSelected()){ 
        Doctor d= new Doctor();
           
            d.setName(nameTf.getText());
            d.setEmpId(empIDTf.getText());
            d.setRole(roleTf.getText());
            d.setEducation(eduTf.getText());
            d.setField(FieldTf.getText());
            employees.add(d);
            //clearFields();
        }else if(nurseRb.isSelected()){
            Nurse n= new Nurse();
            n.setName(nameTf.getText());
            n.setEmpId(empIDTf.getText());
            n.setRole(roleTf.getText());
            n.setEducation(eduTf.getText());
            n.setMonths(monthsTf.getText());
            employees.add(n);
        }else if(MaintenanceRb.isSelected()){
            Maintenance m = new Maintenance();
            m.setName(nameTf.getText());
            m.setEmpId(empIDTf.getText());
            m.setRole(roleTf.getText());
            m.setHours(Double.parseDouble(hoursTf.getText()));
            m.setFloor(floorTf.getText());
            employees.add(m);
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void displayBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayBtnActionPerformed
       if(employees.isEmpty()){
            JOptionPane.showMessageDialog(null,"No employees in this system");
        }
        else{
            for(int i = 0; i < employees.size();i++){
                JOptionPane.showMessageDialog(null,employees.get(i).getDetails());
            }
        }
    }//GEN-LAST:event_displayBtnActionPerformed

