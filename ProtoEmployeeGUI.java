    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        if(employees.isEmpty()){
            JOptionPane.showMessageDialog(null,"No employees to search");
        }
        else{
            String searchTerm = empIDTf.getText();
            for(ProtoEmployee e:employees){
                if(e.getEmpId().equalsIgnoreCase(searchTerm)){
                    JOptionPane.showMessageDialog(null,e.getDetails());
                }
            }
        }    
    }//GEN-LAST:event_searchBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        if(employees.isEmpty()){
            JOptionPane.showMessageDialog(null,"System empty, nothing can be deleted");
        }
        else{
            String searchTerm = empIDTf.getText();
            for(int i = 0; i < employees.size();i++){
                ProtoEmployee e = employees.get(i);
                if(e.getEmpId().equalsIgnoreCase(searchTerm)){
                    employees.remove(e);
                }
            }
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

